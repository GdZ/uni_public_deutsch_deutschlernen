getPages()
{
	page_url=$1
	name=$2
	rdir=`pwd`
	mkdir -p $name

	cd $name
	# pwd
	# wget $page_url -o $name.log -O $name.html

	# find pdf and mp3
	# cat $name.html | grep '/overlay/media/de/' | cut -d'"' -f2
	mp3_urls=`cat $name.html | grep '/overlay/media/de/' | cut -d'"' -f2`
	# echo $mp3_urls
	# echo '----------------------'
	for a in $mp3_urls; do
		folder=`echo $a | cut -d'/' -f5 | cut -d'-' -f1`
		page=`echo $a | cut -d'/' -f6`
		# pwd
		# wget www.dw.de$a -o $page.log -O $page.html
		mp3_url=`cat $page.html | grep -E '.mp3|.mp4|.flv' | grep 'input' | awk '{print $4}' | cut -d'"' -f2`
		case $mp3_url in
			'')
				echo "[get.mp3] ERROR!!!!"
				;;
			*)
				echo "[get.mp3] $mp3_url"
				wget $mp3_url -o mp3.log
				;;
		esac
		echo '-------------------------'
	done

	cd $rdir
}

